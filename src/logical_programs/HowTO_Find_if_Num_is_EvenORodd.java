package logical_programs;

public class HowTO_Find_if_Num_is_EvenORodd 
{
	public static void main(String[] args) 
	{
		
		
		int num=4;
		
		
		//4%2=0==0
		if(num%2==0)
		{
			System.out.println("given numer is even");
		}
		else
		{
			System.out.println("given numer is odd");
		}
		
		
	}
}
